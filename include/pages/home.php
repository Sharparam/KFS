<h2 class="center_text">Vet du att det finns en filmstudio i Kristinehamn?</h2>
<p class="lead">Vill du se filmer från hela världen och från andra kulturer,
    filmer som kan vara svåra att hitta på vår lokala biorepertoar?
    Du vet väl att det finns en filmstudio i Kristinehamn, som visat långfilm på bio sedan 1956.</p>
<p>Filmstudions har som syfte att visa ett brett utbud av värdefull långfilm, filmer som roar och berör.
    En vardagskväll varannan vecka under perioden oktober-mars brukar vi visa våra filmer,
    ett uppskattat inslag i vintermörkret för våra närmare 200 medlemmar.</p>
<p>Våra visningar sker på Nya Grand Bio, Folkets Hus, <strong>tisdagar kl. 15.30</strong> och <strong>18.15</strong>.</p>

<p>Medlemskorten för vårterminen 2017 är nu slutsålda!</p>

